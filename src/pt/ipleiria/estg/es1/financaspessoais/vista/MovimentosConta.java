    private void actualizarDadosConta() {
        lblNome.setText(conta.getNome());
        lblTipo.setText(conta.getTipo().toString());
        lblSaldo.setText(Double.toString(conta.getSaldo()));
    }
    
    private void actualizarMovimentos() {
        DefaultListModel modelo=new DefaultListModel();
        for (Movimento m: conta.getMovimentos())
            modelo.addElement(m);
        lstMovimentos.setModel(modelo);
    }
