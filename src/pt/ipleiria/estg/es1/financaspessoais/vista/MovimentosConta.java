    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
        Movimento m=new Movimento("", 0, TipoMovimento.DEBITO);
        DadosMovimento dialog=new DadosMovimento((Frame)getParent(), m);
        dialog.setVisible(true);
        if (dialog.isAceite()) {
            conta.adicionarMovimento(m);
            actualizarMovimentos();
            actualizarDadosConta();
        }
    }//GEN-LAST:event_btnRegistarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Movimento m=(Movimento)lstMovimentos.getSelectedValue();
        if (JOptionPane.showConfirmDialog(this, "Tem a certeza?")==JOptionPane.YES_OPTION) {
            conta.removerMovimento(m);
            actualizarMovimentos();
            actualizarDadosConta();
        
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void lstMovimentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstMovimentosMouseClicked
        if (evt.getClickCount()>=2) {
            
            Movimento m=(Movimento)lstMovimentos.getSelectedValue();
            Movimento antigo=null;
            try {
                antigo = (Movimento)m.clone();
            } catch (CloneNotSupportedException ex) {
                Logger.getLogger(MovimentosConta.class.getName()).log(Level.SEVERE, null, ex);
            }
            DadosMovimento dialog=new DadosMovimento((Frame)getParent(), m);
            dialog.setVisible(true);
            if (dialog.isAceite()) {
                conta.actualizarSaldo(antigo, m);
                actualizarMovimentos();
                actualizarDadosConta();
            }
        }
    }//GEN-LAST:event_lstMovimentosMouseClicked

    
